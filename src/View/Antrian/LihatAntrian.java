   private void loadReferensi() {
        try {
            // Pasien
            listPasien = daoPasien.getAll();
            cmbPasien.removeAllItems();
            mapPasien.clear();
            for (ModelPasien p : listPasien) {
                cmbPasien.addItem(p.getId() + " - " + p.getNama());
                mapPasien.put(p.getId(), p.getNama());
            }

            // Dokter
            listDokter = daoDokter.getAll();
            cmbDokter.removeAllItems();
            mapDokter.clear();
            for (ModelDokter d : listDokter) {
                cmbDokter.addItem(d.getId() + " - dr. " + d.getNama());
                mapDokter.put(d.getId(), "dr. " + d.getNama());
            }

            // Obat
            listObat = daoObat.getAll();
            cmbObat.removeAllItems();
            mapObat.clear();
            for (ModelObat o : listObat) {
                cmbObat.addItem(o.getId() + " - " + o.getNama());
                mapObat.put(o.getId(), o.getNama());
            }

        } catch (RuntimeException e) {
            JOptionPane.showMessageDialog(this,
                "Gagal load data referensi:\n" + e.getMessage(),
                "Error DB", JOptionPane.ERROR_MESSAGE);
        }
    }
    private void loadTabel() {
        try {
            tableModel.setData(daoAntrian.getAll(), mapPasien, mapDokter, mapObat);
            tableAntrian.setModel(tableModel);
            tableModel.fireTableDataChanged();
        } catch (RuntimeException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error DB", JOptionPane.ERROR_MESSAGE);
        }
    }
