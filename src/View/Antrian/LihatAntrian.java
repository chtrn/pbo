    private void btnDaftarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDaftarActionPerformed
        selectedId = -1; // paksa mode INSERT
        ModelAntrian a = formKeAntrian();
        if (a == null) return;
        try {
            daoAntrian.insert(a);
            loadTabel();
            bersihForm();
            JOptionPane.showMessageDialog(this, "Antrian berhasil didaftarkan!", "Sukses",
                JOptionPane.INFORMATION_MESSAGE);
        } catch (RuntimeException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDaftarActionPerformed

    private void btnUbahStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahStatusActionPerformed
        if (selectedId < 0) {
            JOptionPane.showMessageDialog(this, "Pilih antrian dari tabel terlebih dahulu!",
                "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String statusBaru = (String) cmbStatus.getSelectedItem();
        try {
            daoAntrian.updateStatus(selectedId, statusBaru);
            loadTabel();
            JOptionPane.showMessageDialog(this,
                "Status antrian berhasil diubah menjadi: " + statusBaru, "Sukses",
                JOptionPane.INFORMATION_MESSAGE);
        } catch (RuntimeException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUbahStatusActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (selectedId < 0) {
            JOptionPane.showMessageDialog(this, "Pilih antrian dari tabel terlebih dahulu!",
                "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int konfirmasi = JOptionPane.showConfirmDialog(this,
            "Yakin ingin menghapus antrian ini?", "Konfirmasi Hapus",
            JOptionPane.YES_NO_OPTION);
        if (konfirmasi == JOptionPane.YES_OPTION) {
            try {
                daoAntrian.delete(selectedId);
                loadTabel();
                bersihForm();
            } catch (RuntimeException e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tableAntrianMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableAntrianMouseClicked
        int row = tableAntrian.getSelectedRow();
        if (row < 0) return;

        selectedId = (Integer) tableModel.getValueAt(row, 0);

        // cari antrian dari DAO berdasarkan ID
        try {
            for (ModelAntrian a : daoAntrian.getAll()) {
                if (a.getId().equals(selectedId)) {
                    // set combobox pasien
                    for (int i = 0; i < listPasien.size(); i++)
                        if (listPasien.get(i).getId().equals(a.getIdPasien())) {
                            cmbPasien.setSelectedIndex(i); break;
                        }
                    // set combobox dokter
                    for (int i = 0; i < listDokter.size(); i++)
                        if (listDokter.get(i).getId().equals(a.getIdDokter())) {
                            cmbDokter.setSelectedIndex(i); break;
                        }
                    // set combobox obat
                    for (int i = 0; i < listObat.size(); i++)
                        if (listObat.get(i).getId().equals(a.getIdObat())) {
                            cmbObat.setSelectedIndex(i); break;
                        }
                    txtTanggal.setText(a.getTanggal().toString());
                    txtKeluhan.setText(a.getKeluhan());
                    cmbStatus.setSelectedItem(a.getStatus());
                    break;
                }
            }
        } catch (RuntimeException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tableAntrianMouseClicked
    private ModelAntrian formKeAntrian() {
        if (listPasien.isEmpty() || listDokter.isEmpty() || listObat.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Data pasien, dokter, atau obat belum ada!\nSilakan tambahkan terlebih dahulu.",
                "Peringatan", JOptionPane.WARNING_MESSAGE);
            return null;
        }

        int idxPasien = cmbPasien.getSelectedIndex();
        int idxDokter = cmbDokter.getSelectedIndex();
        int idxObat   = cmbObat.getSelectedIndex();

        if (idxPasien < 0 || idxDokter < 0 || idxObat < 0) {
            JOptionPane.showMessageDialog(this, "Pilih Pasien, Dokter, dan Obat!",
                "Peringatan", JOptionPane.WARNING_MESSAGE);
            return null;
        }

        String tglStr  = txtTanggal.getText().trim();
        String keluhan = txtKeluhan.getText().trim();
        String status  = (String) cmbStatus.getSelectedItem();

        if (tglStr.isEmpty() || keluhan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tanggal dan Keluhan wajib diisi!",
                "Peringatan", JOptionPane.WARNING_MESSAGE);
            return null;
        }

        Date tanggal;
        try {
            tanggal = Date.valueOf(tglStr);
        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this, "Format tanggal harus yyyy-MM-dd\nContoh: 2025-05-24",
                "Error", JOptionPane.ERROR_MESSAGE);
            return null;
        }

        int idPasien = listPasien.get(idxPasien).getId();
        int idDokter = listDokter.get(idxDokter).getId();
        int idObat   = listObat.get(idxObat).getId();

        if (selectedId > 0)
            return new ModelAntrian(selectedId, idPasien, idDokter, idObat, tanggal, keluhan, status);
        else
            return new ModelAntrian(idPasien, idDokter, idObat, tanggal, keluhan, status);
    }
    private void bersihForm() {
        selectedId = -1;
        if (cmbPasien.getItemCount() > 0) cmbPasien.setSelectedIndex(0);
        if (cmbDokter.getItemCount() > 0) cmbDokter.setSelectedIndex(0);
        if (cmbObat.getItemCount()   > 0) cmbObat.setSelectedIndex(0);
        txtTanggal.setText(Date.valueOf(LocalDate.now()).toString());
        txtKeluhan.setText("");
        cmbStatus.setSelectedIndex(0);
        tableAntrian.clearSelection();
    }
