    private void loadTable(){

        DefaultTableModel model =
        (DefaultTableModel)
        tableObat.getModel();

        model.setRowCount(0);


        List<ModelObat> list =
                dao.getAll();

        for(ModelObat o : list){

            model.addRow(

            new Object[]{

                o.getId(),
                o.getNama(),
                o.getKategori(),
                o.getStok(),
                o.getHarga()

            });

        }

    }

