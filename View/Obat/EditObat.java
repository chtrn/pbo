    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try{

    int row =
            tableObat.getSelectedRow();

    if(row==-1){

        JOptionPane.showMessageDialog(
                null,
                "Pilih data");

        return;
    }

    ModelObat obat =
            new ModelObat();

    obat.setId(
Integer.parseInt(
tableObat.getValueAt(
row,0).toString()));

    obat.setNama(
tableObat.getValueAt(
row,1).toString());

    obat.setKategori(
tableObat.getValueAt(
row,2).toString());

    obat.setStok(
Integer.parseInt(
tableObat.getValueAt(
row,3).toString()));

    obat.setHarga(
Double.parseDouble(
tableObat.getValueAt(
row,4).toString()));

    new UpdateObat(obat)
            .setVisible(true);

    dispose();

}

catch(Exception e){

    JOptionPane.showMessageDialog(
            null,
            e.getMessage());

}



    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row =
tableObat.getSelectedRow();

if(row==-1){

    JOptionPane.showMessageDialog(
            null,
            "Pilih data");

    return;
}

int id =
Integer.parseInt(
tableObat.getValueAt(
row,0).toString());

dao.delete(id);

JOptionPane.showMessageDialog(
        null,
        "Data dihapus");

loadTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

